namespace UnityEngine.InputSystem.OldInputCompatibility
{
    internal class ActionNameMapper
    {
        public static string GetKeyboardAnyKeyActionName()
        {
            return $"Keyboard_AnyKey";
        }
        public static string GetKeyboardActionNameForKeyCode(string keyCodeName)
        {
            return $"Key_{keyCodeName}";
        }

        public static string GetKeyboardActionNameForKeyCode(KeyCode keyCode)
        {
            return GetKeyboardActionNameForKeyCode(keyCode.ToString());
        }

        public static string GetAxisActionNameFromAxisName(string axisName)
        {
            return $"Axis_{axisName}";
        }
    }
}